# Não se pode utilizar o mesmo tipo de aspas dentro de uma mesma string.

# Aspas simples
print('Gustavo " Henrique')

# Aspas duplas
print("Gustavo ' Henrique")

# Escape
print('Gustavo \\ \" Henrique')

# r
print(r"Gustavo \\ \" \' Henrique")

print('Python é uma linguagem de tipagem forte e dinâmica.')
